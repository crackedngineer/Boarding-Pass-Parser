export function groupFlightsByPnr(flights: Flight[]) {
  const map = new Map<string, Flight[]>()
  for (const f of flights) {
    const key = f.pnr ?? f.id
    if (!map.has(key)) map.set(key, [])
    map.get(key)!.push(f)
  }
  return Array.from(map.values()).map(legs => {
    const sorted = [...legs].sort(
      (a, b) =>
        a.date.localeCompare(b.date) || a.departure_time.localeCompare(b.departure_time),
    )
    return {
      pnr: sorted[0].pnr ?? sorted[0].id,
      legs: sorted,
      isConnecting: sorted.length > 1,
    }
  })
}
